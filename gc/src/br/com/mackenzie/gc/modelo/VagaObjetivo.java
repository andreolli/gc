package br.com.mackenzie.gc.modelo;

public class VagaObjetivo {
	
	private int idObejtivo;
	private String area;
	private int nivel;
	
	public int getIdObejtivo() {
		return idObejtivo;
	}
	public void setIdObejtivo(int idObejtivo) {
		this.idObejtivo = idObejtivo;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public int getNivel() {
		return nivel;
	}
	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	
	
	

}
